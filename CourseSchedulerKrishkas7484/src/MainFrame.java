    private void scheduleSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleSubmitButtonActionPerformed
        // TODO add your handling code here:
        String Status = "";
        String Status_print = "";
        String courseCode = (String) courseComboBox.getSelectedItem();
       int index = studentComboBox.getSelectedIndex();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(index).getStudentID();
       if (CourseQueries.getAllCourseSeats(currentSemester,courseCode) > ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode)){
            Status = "S";
            Status_print = "scheduled";
        }
        else{
            Status = "W";
            Status_print = "waitlisted";
        }
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        ScheduleEntry entry = new ScheduleEntry(currentSemester,courseCode,studentID,Status,currentTimestamp);
        ScheduleQueries.addScheduleEntry(entry);
        scheduleStatusLabel.setText(students.get(index).getLastName() + "," + students.get(index).getFirstName() + " has been "+ Status_print+  " successfully to "+courseCode+".");
    }//GEN-LAST:event_scheduleSubmitButtonActionPerformed

