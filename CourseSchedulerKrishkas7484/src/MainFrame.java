    public void rebuildSemesterComboBoxes(){
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        changeSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester=semesters.get(0);
        }
        else{
            currentSemester=changeSemesterComboBox.getSelectedItem().toString();
            currentSemesterLabel.setText(currentSemester);
        }
    }
    
    public void rebuildStudentComboBoxes(){
        ArrayList<StudentEntry> students= StudentQueries.getAllStudents();
        ArrayList<String> students_string = new ArrayList<String>();
        if (students.size() > 0){
            currentStudent = students.get(0);
            currentStudentLabel.setText(currentStudent.getLastName()+", "+currentStudent.getFirstName()+" "+currentStudent.getStudentID());
        }
        for (StudentEntry student:students){
            students_string.add(student.getLastName()+","+student.getFirstName());
        }
        studentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students_string.toArray()));
        StudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students_string.toArray()));
        if (students_string.size()==0){
            currentStudentLabel.setText("");
        }
    }
    
    public void rebuildCourseComboBoxes(){
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(currentSemester);
        courseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));  
        courseComboBox2.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        courseComboBox3.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
    }
    public void rebuildStudentCoursesComboBox(){
        if (currentStudent != null){
            String studentID = currentStudent.getStudentID();
            ArrayList<ScheduleEntry> entries = ScheduleQueries.getScheduleByStudent(currentSemester,studentID);
        
            ArrayList<String> courseCode_string = new ArrayList<String>();
        
            for (ScheduleEntry entry:entries){
                courseCode_string.add(entry.getCourseCode());
            }
            studentCoursesComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCode_string.toArray()));
        } 
    }
    
